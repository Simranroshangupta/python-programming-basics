# Set Basics in Python
"""WHAT: Set is an unordered collection of unique elements.
WHY: Used to remove duplicate values.
HOW: Duplicate values are ignored automatically."""

# Creating a set
numbers = {1, 2, 3, 4, 4, 5}

# Printing the set
print("Set:", numbers)

# Sets store only unique elements
print("Unique elements only")

# Length of set
print("Length of set:", len(numbers))
