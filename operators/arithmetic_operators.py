# Arithmetic Operators in Python

a = 10
b = 3

print("a =", a)
print("b =", b)

# Addition
print("Addition:", a + b)

# Subtraction
print("Subtraction:", a - b)

# Multiplication
print("Multiplication:", a * b)

# Division
print("Division:", a / b)

# Modulus (remainder)
print("Modulus:", a % b)

# Floor Division
print("Floor Division:", a // b)

# Exponentiation
print("Exponent:", a ** b)
