# RETURN SINGLE VALUE
# Meaning:
# A function can return a single value using the return statement.
# The returned value can be stored and reused.

def add(a, b):
    return a + b

def square(num):
    return num * num

# Calling functions
result1 = add(10, 20)
result2 = square(5)

print("Sum:", result1)
print("Square:", result2)
