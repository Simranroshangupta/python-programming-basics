# HELP FUNCTION IN PYTHON
# Meaning:
# help() is a built-in Python function used to display documentation
# about functions, data types, methods, and modules.

# Getting help about the print function
help(print)

# Getting help about string methods
help(str)

# Getting help about list methods
help(list)

# Getting help about a specific method
text = "python"
help(text.upper)
