# LIST INDEXING
# Meaning:
# Indexing is used to access individual elements from a list.
# Index starts from 0 (left to right) and -1 (right to left).

numbers = [10, 20, 30, 40, 50]

# Access elements using positive index
print("Index 0:", numbers[0])
print("Index 2:", numbers[2])

# Access elements using negative index
print("Index -1:", numbers[-1])
print("Index -3:", numbers[-3])
