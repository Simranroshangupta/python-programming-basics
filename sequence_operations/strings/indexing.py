# STRING INDEXING
# Meaning:
# Indexing is used to access individual characters in a string.
# Index starts from 0 (left to right) and -1 (right to left).

text = "PYTHON"

# Access characters using positive index
print("Index 0:", text[0])
print("Index 1:", text[1])
print("Index 2:", text[2])

# Access characters using negative index
print("Index -1:", text[-1])
print("Index -2:", text[-2])
