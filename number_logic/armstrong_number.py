# Program to check whether a number is an Armstrong number

ognum = int(input("Enter your number: "))

num = ognum
result = 0

while num > 0:
    digit = num % 10
    cube = digit * digit * digit
    result = result + cube
    num = int(num / 10)

print(num)

if ognum == result:
    print(f"It is an Armstrong number")
else:
    print(f"It is Not an Armstrong number")
