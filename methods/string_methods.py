# STRING METHODS
# Meaning:
# String methods are built-in functions that work on string objects.
# They help manipulate and analyze text data.

text = "  Python Programming  "

# Converts string to uppercase
print(text.upper())

# Converts string to lowercase
print(text.lower())

# Removes leading and trailing spaces
print(text.strip())

# Replaces a word in the string
print(text.replace("Python", "Java"))

# Finds position of a word
print(text.find("Programming"))

# Checks if string starts with a word
print(text.startswith("Python"))
